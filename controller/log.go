func GetLogsStatBreakdown(c *gin.Context) {
	dimension := c.Query("dimension")
	if dimension == "" {
		dimension = "channel"
	}
	startTimestamp, _ := strconv.ParseInt(c.Query("start_timestamp"), 10, 64)
	endTimestamp, _ := strconv.ParseInt(c.Query("end_timestamp"), 10, 64)
	tokenName := c.Query("token_name")
	username := c.Query("username")
	usernames := parseQueryList(c.Query("usernames"))
	modelName := c.Query("model_name")
	channel, _ := strconv.Atoi(c.Query("channel"))
	group := c.Query("group")
	rows, err := model.SumQuotaBreakdown(dimension, startTimestamp, endTimestamp, modelName, username, usernames, tokenName, channel, group)
	if err != nil {
		common.ApiError(c, err)
		return
	}
	// 渠道维度回填渠道名称。
	if dimension == "channel" {
		if nameMap, mapErr := model.GetChannelIdNameMap(); mapErr == nil {
			for i := range rows {
				if name, ok := nameMap[rows[i].ChannelId]; ok && name != "" {
					rows[i].Name = name
				}
			}
		}
	}
	// 额外返回不分组的真实总额（与日志页 SumUsedQuota 同口径，不受分组 1000 条上限影响），
	// 供弹窗合计直接使用，保证与 /console/log 统计一致。
	totalStat, _ := model.SumUsedQuota(0, startTimestamp, endTimestamp, modelName, username, usernames, tokenName, channel, group)
	c.JSON(http.StatusOK, gin.H{
		"success": true,
		"message": "",
		"data": gin.H{
			"rows":          rows,
			"total_consume": totalStat.Quota,
			"total_refund":  totalStat.RefundQuota,
		},
	})
	return
}

